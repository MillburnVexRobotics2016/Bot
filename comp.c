//Drive function for the autonomous section
void drive(int direction){
	int speed = 100;
	if(direction == 1){
		//forwards
		motor[FL] = -speed;
		motor[FR] = speed;
		motor[BL] = speed;
		motor[BR] = -speed;
	}else if(direction == 2){
		//backwards
		motor[FL] = speed;
		motor[FR] = -speed;
		motor[BL] = -speed;
		motor[BR] = speed;
	}else if(direction == 3){
		//turn left
	  motor[FL] = speed;
		motor[FR] = speed;
		motor[BL] = -speed;
		motor[BR] = -speed;
	}else if(direction == 4){
		//turn right
		motor[FL] = -speed;
		motor[FR] = -speed;
		motor[BL] = speed;
		motor[BR] = speed;
	}else{
		//stop
	  speed = 0;
		motor[FL] = speed;
		motor[FR] = speed;
		motor[BL] = speed;
		motor[BR] = speed;
	}
}

