//autonomous code here
task autonomous()
{
	drive(FORWARDS);
	wait1Msec(1000);
	drive(STOP);

	clawclose(127);
	wait1Msec(700);
	clawopen(0);

	drive(TURNRIGHT);
	wait1Msec(500);
	drive(STOP);

	drive(BACKWARDS);
	wait1Msec(1000);
	drive(STOP);

	slaveMotor(TLArms, BArms);
	slaveMotor(TRArms, BArms);
	while(SensorValue[armPo] <= 1700){
		liftup(127);
	}
	if(SensorValue[armPo] >= 1700){
		liftup(0);
	}
	if(SensorValue[armPo] > 1500){
		motor[Claws] = 127;
	}
	if(SensorValue[armPo] > 1700){
		motor[Claws] = 0;
		motor[BArms] = 0;
	}

	wait1Msec(200);
	while(SensorValue[armPo] <= 1700 && SensorValue[armPo] > 0){
		liftdown(127);
	}



}


