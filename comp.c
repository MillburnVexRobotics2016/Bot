//drive sub task
//called in the main task
task driveM()
{
	int deadzone = 20;
	while(true)
	{
		if(vexRT[Ch3] > deadzone || vexRT[Ch3] < -deadzone || vexRT[Ch1] > deadzone || vexRT[Ch1] < -deadzone)
		{
			motor[FL] = -vexRT[Ch3] - vexRT[Ch1];
			motor[BL] = vexRT[Ch3] + vexRT[Ch1];
			motor[FR] = vexRT[Ch3] - vexRT[Ch1];
			motor[BR] = -vexRT[Ch3] + vexRT[Ch1];
		}
		else
		{
			motor[FL] = motor[BL] = motor[FR] = motor[BR] = 0;
		}
	}
}


