task driveM()
{
	int deadzone = 20;
	int FL;
	int FR;
	int BL;
	int BR;
	while(true)
	{
		FL = vexRT[Ch3] + vexRT[Ch1] + vexRT[Ch4];
		BL = vexRT[Ch3] + vexRT[Ch1] - vexRT[Ch4];
		FR = -(vexRT[Ch3] - vexRT[Ch1] - vexRT[Ch4]);
		BR = -(vexRT[Ch3] - vexRT[Ch1] + vexRT[Ch4]);

		if(abs(FL) > deadzone)
		{
			motor[frontLeft] = FL;
		}
		else
		{
			motor[frontLeft] = 0;
		}
		if(abs(BL) > deadzone)
		{
			motor[backLeft] = BL;
		}
		else
		{
			motor[backLeft] = 0;
		}
		if(abs(FR) > deadzone)
		{
			motor[frontRight] = FR;
		}
		else
		{
			motor[frontRight] = 0;
		}
		if(abs(BR) > deadzone)
		{
			motor[backRight] = BR;
		}
		else
		{
			motor[backRight] = 0;
		}
	}
}


task main()
{
	int xL = 127;
	int xR = 117;

	int clawRotSpeed = 20;

	startTask(driveM);
	while(true)
	{

	  if(vexRT[Btn6U] && !vexRT[Btn6D]){
	  	motor[clawRot] = clawRotSpeed;
	  }else if(vexRT[Btn6D] && !vexRT[Btn6U]){
	  	motor[clawRot] = -clawRotSpeed;
	  }else{
	  	motor[clawRot] = 0;
	  }

		//processJoysttrrig iuiugt

		//adjusts the speeds of the scissor lift motors
		if(vexRT[Btn8L] && xL <= 117) xL = xL + 10;

		if(vexRT[Btn8U] && xR <= 117) xR = xR + 10;

		if(vexRT[Btn8D] && xL >= 10) xL = xL - 10;

		if(vexRT[Btn8R] && xR >= 10) xR = xR - 10;

		if(vexRT[Btn6U]){
			xL = 127;
			xR = 117;
		}
		//raises and lowers the scissor lift
		if(vexRT[Btn7L] == 1) {
			motor[xLeft] = -xL;
			motor[xRight] = xR;
		}
		else if(vexRT[Btn7D] == 1) {
			motor[xLeft] = xL;
			motor[xRight] = -xR;
		}
		else {
			motor[xLeft] = 0;
			motor[xRight] = 0;
		}
	}
}
